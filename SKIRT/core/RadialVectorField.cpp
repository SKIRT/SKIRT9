#include "RadialVectorField.hpp"

//////////////////////////////////////////////////////////////////////

int RadialVectorField::dimension() const
{
    return 1;
}

//////////////////////////////////////////////////////////////////////

Vec RadialVectorField::vector(Position bfr) const
{
    double norm = bfr.norm();
    return norm > 0. ? bfr / norm : Vec();
}


#include "AxGeometry.hpp"

//////////////////////////////////////////////////////////////////////

int AxGeometry::dimension() const
{
    return 2;
}

//////////////////////////////////////////////////////////////////////

double AxGeometry::density(Position bfr) const
{
    return density(bfr.cylRadius(), bfr.height());
}

//////////////////////////////////////////////////////////////////////

double AxGeometry::SigmaX() const
{
    return 2.0 * SigmaR();
}

//////////////////////////////////////////////////////////////////////

double AxGeometry::SigmaY() const
{
    return 2.0 * SigmaR();
}


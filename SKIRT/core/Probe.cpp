#include "Probe.hpp"

////////////////////////////////////////////////////////////////////

Probe::When Probe::when() const
{
    return When::Setup;
}

////////////////////////////////////////////////////////////////////

void Probe::initialize() {}

////////////////////////////////////////////////////////////////////

string Probe::itemName() const
{
    return probeName();
}

////////////////////////////////////////////////////////////////////

int Probe::iter() const
{
    return _iter;
}

////////////////////////////////////////////////////////////////////

void Probe::probeSetup()
{
    initialize();
    if (when() == When::Setup) probe();
}

////////////////////////////////////////////////////////////////////

void Probe::probeRun()
{
    if (when() == When::Run) probe();
}

////////////////////////////////////////////////////////////////////

void Probe::probePrimary(int iter)
{
    if (when() == When::Primary)
    {
        _iter = iter;
        probe();
        _iter = 0;
    }
}

////////////////////////////////////////////////////////////////////

void Probe::probeSecondary(int iter)
{
    if (when() == When::Secondary)
    {
        _iter = iter;
        probe();
        _iter = 0;
    }
}

